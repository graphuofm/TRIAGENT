"""L4: Edge-Side SDI Predictor.

Pipeline:
    1. Load sdi_data.csv
    2. Mine n-gram triggers (uni / bi / tri) from the high-SDI subset
    3. Extract ~20 edge features per sentence
    4. Train 5 ablation models (random / LR-uni / LR-uni+bi / LR-uni+bi+tri / XGBoost)
    5. Generate ROC plot + feature-importance plot + LaTeX ablation table

Run:
    python experiments/L4_predictor.py
    python experiments/L4_predictor.py --target sdi_max --threshold 0.7
"""
from __future__ import annotations

import argparse
import sys
from pathlib import Path

import numpy as np
import pandas as pd
import matplotlib.pyplot as plt
from sklearn.metrics import roc_curve

sys.path.insert(0, str(Path(__file__).parent.parent))

from src.config import RESULTS_DATA_DIR, FIGURES_DIR, TABLES_DIR, LEXICONS_DIR
from src.mining.log_odds import mine_ngram_triggers, save_top_triggers
from src.predictor.features import (
    extract_features, load_trigger_lexicon, feature_columns_for,
)
from src.predictor.sentence_features import (
    add_sentence_features_to_df, add_llm_reasoning_features_to_df,
)
from src.predictor.train import (
    train_split, train_random, train_lr, train_xgb, lr_top_features,
)
from src.viz.style import apply_style


def fig_predictor_auc(models: list, out_path: Path) -> None:
    fig, ax = plt.subplots(figsize=(5.5, 4.5))
    for m in models:
        if len(np.unique(m.test_y)) < 2:
            continue
        fpr, tpr, _ = roc_curve(m.test_y, m.test_pred_proba)
        ax.plot(fpr, tpr, lw=1.6,
                label=f"{m.name}  (AUC={m.metrics['auc_roc']:.3f})")
    ax.plot([0, 1], [0, 1], '--', color='#888', lw=0.8)
    ax.set_xlabel('False positive rate')
    ax.set_ylabel('True positive rate')
    ax.set_title('Edge-side SDI predictor — ROC')
    ax.legend(loc='lower right', fontsize=8)
    ax.set_xlim(0, 1); ax.set_ylim(0, 1)
    fig.tight_layout()
    fig.savefig(out_path)
    plt.close(fig)
    print(f"  ✓ saved {out_path}")


def fig_feature_importance(top_feats: pd.DataFrame, out_path: Path,
                           model_name: str) -> None:
    fig, ax = plt.subplots(figsize=(6, 4))
    colors = ['#d62728' if c < 0 else '#1f77b4' for c in top_feats['coef']]
    ax.barh(top_feats['feature'][::-1], top_feats['coef'][::-1], color=colors[::-1])
    ax.axvline(0, color='black', lw=0.6)
    ax.set_xlabel('LR coefficient (positive ⇒ predicts high SDI)')
    ax.set_title(f'Top features — {model_name}')
    fig.tight_layout()
    fig.savefig(out_path)
    plt.close(fig)
    print(f"  ✓ saved {out_path}")


def write_ablation_table(rows: list[dict], out_path: Path) -> pd.DataFrame:
    df = pd.DataFrame(rows)
    print("\n=== Ablation summary ===")
    print(df.to_string(index=False, float_format=lambda x: f"{x:.4f}"))
    with open(out_path, 'w') as f:
        f.write("% Auto-generated by experiments/L4_predictor.py\n")
        f.write("\\begin{tabular}{lrrrrr}\n\\toprule\n")
        f.write("Model & AUC-ROC & AUC-PR & P@R20 & P@R50 & P@top10 \\\\\n\\midrule\n")
        for _, r in df.iterrows():
            f.write(
                f"{r['model']} & {r['auc_roc']:.3f} & {r['auc_pr']:.3f} & "
                f"{r['p_at_r20']:.3f} & {r['p_at_r50']:.3f} & "
                f"{r['p_at_top10']:.3f} \\\\\n"
            )
        f.write("\\bottomrule\n\\end{tabular}\n")
    print(f"  ✓ saved {out_path}")
    return df


def main(args):
    apply_style()
    src_csv = RESULTS_DATA_DIR / 'sdi_data.csv'
    if not src_csv.exists():
        raise FileNotFoundError(f"Run L2 first to produce {src_csv}")
    df = pd.read_csv(src_csv)
    print(f"Loaded {len(df)} rows from {src_csv}")

    # 1) Mine triggers from the high-SDI subset
    print(f"\nMining n-gram triggers (target={args.target}>{args.threshold})...")
    triggers = mine_ngram_triggers(df, target_col=args.target,
                                   target_threshold=args.threshold,
                                   ngram_range=(1, 3), min_df=args.min_df)
    print(f"  total candidates: {len(triggers)}")
    print(f"  by n: {triggers['n'].value_counts().to_dict()}")
    print("\n  Top 8 unigram triggers (positive z = predicts high SDI):")
    print(triggers[triggers['n'] == 1].head(8)[['ngram', 'z_score', 'log_odds',
                                                'presence_high_sdi', 'presence_low_sdi']].to_string(index=False))
    print("\n  Top 8 bigram triggers:")
    print(triggers[triggers['n'] == 2].head(8)[['ngram', 'z_score', 'log_odds',
                                                'presence_high_sdi', 'presence_low_sdi']].to_string(index=False))
    print("\n  Top 8 trigram triggers:")
    print(triggers[triggers['n'] == 3].head(8)[['ngram', 'z_score', 'log_odds',
                                                'presence_high_sdi', 'presence_low_sdi']].to_string(index=False))

    lex_path = save_top_triggers(triggers, top_per_n=args.top_per_n)
    print(f"  ✓ saved trigger lexicon → {lex_path}")
    lexicon = load_trigger_lexicon(lex_path)

    # 2) Extract features
    print("\nExtracting edge-side features...")
    X = extract_features(df, lexicon)
    y = (df[args.target] > args.threshold).astype(int).to_numpy()
    pos_rate = y.mean()
    print(f"  feature matrix: {X.shape}, positive rate = {pos_rate:.3f}")

    # 3) 80/20 split
    X_tr, X_te, y_tr, y_te = train_split(X, y, test_size=0.2, seed=42)
    print(f"  train={len(X_tr)}, test={len(X_te)}, train pos rate={y_tr.mean():.3f}")

    # 4) Train ablation models — three feature granularities (word/phrase/sentence)
    print("\nTraining models...")
    models = []
    models.append(train_random(y_tr, y_te))
    models.append(train_lr(X_tr, y_tr, X_te, y_te,
                           name='LR-unigram', feature_cols=feature_columns_for('unigram', X)))
    models.append(train_lr(X_tr, y_tr, X_te, y_te,
                           name='LR-uni+bigram', feature_cols=feature_columns_for('unibi', X)))
    models.append(train_lr(X_tr, y_tr, X_te, y_te,
                           name='LR-uni+bi+trigram', feature_cols=feature_columns_for('unibitri', X)))

    # ⭐ NEW: sentence-level (contextual) features via sentence-BERT
    if not args.no_sbert:
        print("\n[L4] Adding sentence-BERT contextual features (PCA-reduced)...")
        # Use train indices to fit PCA without leakage
        train_idx = np.array([i for i in range(len(X)) if i in X_tr.index])
        X_with_sent, sent_cols = add_sentence_features_to_df(
            df.assign(_idx=range(len(df))),  # keep ordering aligned with X
            cache_tag=args.sbert_tag,
            train_idx=train_idx,
            pca_dim=args.sbert_dim,
        )
        # Splice sentence features back into X / X_tr / X_te (same row order)
        for c in sent_cols:
            X[c] = X_with_sent[c].values
            X_tr[c] = X.loc[X_tr.index, c]
            X_te[c] = X.loc[X_te.index, c]

        unibi_cols = feature_columns_for('unibi', X)
        unibitri_cols = feature_columns_for('unibitri', X)
        models.append(train_lr(X_tr, y_tr, X_te, y_te,
                               name='LR-sentence-only',
                               feature_cols=feature_columns_for('vader_struct', X) + sent_cols))
        models.append(train_lr(X_tr, y_tr, X_te, y_te,
                               name='LR-uni+bi+sentence',
                               feature_cols=unibi_cols + sent_cols))
        models.append(train_xgb(X_tr, y_tr, X_te, y_te,
                                feature_cols=unibitri_cols + sent_cols))
    else:
        models.append(train_xgb(X_tr, y_tr, X_te, y_te,
                                feature_cols=feature_columns_for('unibitri', X)))

    # ⭐ Optional non-deployable upper bound: LLM reasoning embedding
    if args.with_reasoning and 'llm_reasoning' in df.columns:
        print("\n[L4] Adding LLM-reasoning embedding (non-deployable upper bound)...")
        train_idx = np.array([i for i in range(len(X)) if i in X_tr.index])
        X_with_reason, reason_cols = add_llm_reasoning_features_to_df(
            df.assign(_idx=range(len(df))),
            cache_tag=args.sbert_tag,
            train_idx=train_idx,
            pca_dim=args.sbert_dim,
        )
        for c in reason_cols:
            X[c] = X_with_reason[c].values
            X_tr[c] = X.loc[X_tr.index, c]
            X_te[c] = X.loc[X_te.index, c]
        sent_cols_local = [c for c in X.columns if c.startswith('sbert_pca_')]
        all_feat = (feature_columns_for('unibi', X) + sent_cols_local + reason_cols)
        models.append(train_xgb(X_tr, y_tr, X_te, y_te,
                                feature_cols=all_feat))
        models[-1].name = 'XGBoost+reasoning (NOT DEPLOYABLE)'

    rows = [{'model': m.name, **m.metrics} for m in models]

    # 5) Outputs
    fig_predictor_auc(models, FIGURES_DIR / 'fig_predictor_auc.png')

    # Top features for the LR-uni+bi model (paper's primary ablation)
    main_lr = next(m for m in models if m.name == 'LR-uni+bigram')
    top = lr_top_features(main_lr, k=12)
    print("\n  Top 12 LR-uni+bigram features:")
    print(top.to_string(index=False))
    fig_feature_importance(top, FIGURES_DIR / 'fig_feature_importance.png',
                           model_name='LR-uni+bigram')

    write_ablation_table(rows, TABLES_DIR / 'predictor_ablation.tex')

    summary = pd.DataFrame(rows)
    summary.to_csv(RESULTS_DATA_DIR / 'predictor_results.csv', index=False)


if __name__ == '__main__':
    parser = argparse.ArgumentParser()
    parser.add_argument('--target', default='sdi_max',
                        help='Column to threshold for positive class')
    parser.add_argument('--threshold', type=float, default=0.7)
    parser.add_argument('--min-df', type=int, default=3)
    parser.add_argument('--top-per-n', type=int, default=50)
    parser.add_argument('--no-sbert', action='store_true',
                        help='Skip sentence-BERT features (faster, less novel ablation)')
    parser.add_argument('--sbert-tag', default='fpb',
                        help='Cache tag for sentence-BERT embeddings')
    parser.add_argument('--sbert-dim', type=int, default=16,
                        help='PCA target dim for sentence-BERT embeddings')
    parser.add_argument('--with-reasoning', action='store_true',
                        help='Add the non-deployable LLM-reasoning embedding ablation')
    args = parser.parse_args()
    main(args)
